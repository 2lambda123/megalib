////////////////////////////////////////////////////////////////////////////////


MString MNeuralNetworkIO::ToString() const
{
  MString S = "";

  for (unsigned int i = 0; i < m_Inputs.size(); ++i) {
    S += i;
    S += ": ";
    S += m_Inputs[i];
    S += "\n";
  }

  return S;
}


